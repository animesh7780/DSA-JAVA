public class Pattern21 {
    public static void main(String[] args) {
        pattern21(5);
    }

    static void pattern21(int n) {
        int num = 1;
        for (int i = 1; i <= n; i++) {
            for (int j = 1; j <= i; j++) {
                System.out.print(num + " ");
                num++;
            }
            System.out.println();
        }
    }
}

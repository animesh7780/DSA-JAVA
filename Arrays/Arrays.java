public class Arrays {
    public static void main(String[] args) {
        int[] arr = new int[5];
        arr[0] = 10;
        arr[1] = 20;
        arr[2] = 30;
        arr[3] = 40;
        arr[4] = 50;
        System.out.println(arr[0]);

        // 0 for int and null for String
        String[] arr1 = new String[5];
        System.out.println(arr1[2]);
    }
}